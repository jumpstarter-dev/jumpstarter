"""Tests for render_proto_source() and its helpers."""

from abc import ABCMeta, abstractmethod
from collections.abc import AsyncGenerator

from jumpstarter_driver_power.driver import PowerInterface

from .descriptor_builder import build_file_descriptor
from .proto_render import (
    _format_comment,
    _resolve_type_name,
    render_proto_source,
)

# ---------------------------------------------------------------------------
# Test interfaces
#
# Follow the current-branch convention: a plain class with metaclass=ABCMeta, a
# client() classmethod, and @abstractmethod interface methods. The builder
# discovers methods purely by introspection.
# ---------------------------------------------------------------------------


class PowerTestInterface(metaclass=ABCMeta):
    """A power control interface."""

    @classmethod
    def client(cls) -> str:
        return "test.PowerClient"

    @abstractmethod
    async def on(self) -> None:
        """Turn power on."""
        ...

    @abstractmethod
    async def off(self) -> None: ...

    @abstractmethod
    async def status(self) -> str: ...


class StreamTestInterface(metaclass=ABCMeta):
    """Interface with streaming."""

    @classmethod
    def client(cls) -> str:
        return "test.StreamClient"

    @abstractmethod
    async def read_values(self) -> AsyncGenerator[float, None]: ...


# ---------------------------------------------------------------------------
# Proto source rendering
# ---------------------------------------------------------------------------


class TestRenderProtoSource:
    """Verify render_proto_source() produces valid .proto text."""

    def test_header_comment(self):
        fd = build_file_descriptor(PowerTestInterface)
        source = render_proto_source(fd)
        assert "DO NOT EDIT" in source

    def test_syntax_line(self):
        fd = build_file_descriptor(PowerTestInterface)
        source = render_proto_source(fd)
        assert 'syntax = "proto3";' in source

    def test_package_line(self):
        fd = build_file_descriptor(PowerTestInterface)
        source = render_proto_source(fd)
        assert "package jumpstarter.interfaces.power_test.v1;" in source

    def test_service_declaration(self):
        fd = build_file_descriptor(PowerTestInterface)
        source = render_proto_source(fd)
        assert "service PowerTestInterface {" in source

    def test_rpc_methods(self):
        fd = build_file_descriptor(PowerTestInterface)
        source = render_proto_source(fd)
        assert "rpc On(" in source
        assert "rpc Off(" in source
        assert "rpc Status(" in source

    def test_empty_import(self):
        fd = build_file_descriptor(PowerTestInterface)
        source = render_proto_source(fd)
        assert 'import "google/protobuf/empty.proto";' in source

    def test_message_definitions(self):
        """Generated messages should appear in the output."""
        fd = build_file_descriptor(PowerTestInterface)
        source = render_proto_source(fd)
        # status returns str, so should have a response message
        assert "message " in source

    def test_streaming_method(self):
        """Server streaming methods should use the 'stream' keyword."""
        fd = build_file_descriptor(StreamTestInterface)
        source = render_proto_source(fd)
        assert "stream" in source

    def test_doc_comments_rendered(self):
        """Docstrings should appear as // comments."""
        fd = build_file_descriptor(PowerTestInterface)
        source = render_proto_source(fd)
        assert "// A power control interface" in source or "// Turn power on" in source


# ---------------------------------------------------------------------------
# Power round-trip (the real PowerInterface)
# ---------------------------------------------------------------------------


class TestPowerRoundTrip:
    """Render the real PowerInterface and verify its structure.

    The current-tree PowerInterface carries no docstrings, so the rendered
    source has the golden structure without the // doc comments.
    """

    def test_power_structure(self):
        fd = build_file_descriptor(PowerInterface)
        source = render_proto_source(fd)

        assert "// Code generated by jmp interface generate. DO NOT EDIT." in source
        assert 'syntax = "proto3";' in source
        assert "package jumpstarter.interfaces.power.v1;" in source
        assert 'import "google/protobuf/empty.proto";' in source
        assert "service PowerInterface {" in source
        assert "rpc Off(google.protobuf.Empty) returns (google.protobuf.Empty);" in source
        assert "rpc On(google.protobuf.Empty) returns (google.protobuf.Empty);" in source
        assert "rpc Read(google.protobuf.Empty) returns (stream PowerReading);" in source
        assert "message PowerReading {" in source
        assert "double voltage = 1;" in source
        assert "double current = 2;" in source

    def test_power_method_order(self):
        """Methods render in the builder's (sorted) order: Off, On, Read."""
        fd = build_file_descriptor(PowerInterface)
        source = render_proto_source(fd)
        off = source.index("rpc Off(")
        on = source.index("rpc On(")
        read = source.index("rpc Read(")
        assert off < on < read


# ---------------------------------------------------------------------------
# Utility functions
# ---------------------------------------------------------------------------


class TestUtilities:
    """Test renderer helper functions."""

    def test_resolve_type_name_same_package(self):
        assert _resolve_type_name(".pkg.v1.MyMsg", "pkg.v1") == "MyMsg"

    def test_resolve_type_name_google(self):
        result = _resolve_type_name(".google.protobuf.Empty", "pkg.v1")
        assert result == "google.protobuf.Empty"

    def test_resolve_type_name_different_package(self):
        result = _resolve_type_name(".other.pkg.Msg", "my.pkg")
        assert result == "other.pkg.Msg"

    def test_format_comment_single_line(self):
        result = _format_comment("Hello world", "  ")
        assert result == "  // Hello world"

    def test_format_comment_multi_line(self):
        result = _format_comment("Line 1\nLine 2", "")
        assert "// Line 1" in result
        assert "// Line 2" in result
