"""Rust driver client package generator for `jmp proto generate --language rust`.

Generates a complete, publishable Rust crate from a FileDescriptorProto:
  1. proto/{name}.proto — bundled proto source for tonic-build
  2. src/lib.rs — typed client struct wrapping tonic gRPC stub with portforward
  3. build.rs — tonic-build proto compilation
  4. Cargo.toml — crate metadata
"""

from __future__ import annotations

import re

from google.protobuf.descriptor_pb2 import (
    DescriptorProto,
    FieldDescriptorProto,
    FileDescriptorProto,
    MethodDescriptorProto,
)

from . import register_language


# ---------------------------------------------------------------------------
# Naming helpers
# ---------------------------------------------------------------------------


def _pascal_to_snake(name: str) -> str:
    """Convert PascalCase to snake_case."""
    s = re.sub(r"([A-Z]+)([A-Z][a-z])", r"\1_\2", name)
    s = re.sub(r"([a-z\d])([A-Z])", r"\1_\2", s)
    return s.lower()


def _resolve_type_name(type_name: str) -> str:
    """Strip leading dot from fully-qualified type names."""
    if type_name.startswith("."):
        return type_name[1:]
    return type_name


def _short_name(full_name: str) -> str:
    """Extract short name from a fully-qualified proto name."""
    return full_name.rsplit(".", 1)[-1]


def _is_empty_type(type_name: str) -> bool:
    resolved = _resolve_type_name(type_name)
    return resolved == "google.protobuf.Empty"


def _proto_filename(fd: FileDescriptorProto) -> str:
    """Derive the proto filename from the package.

    jumpstarter.interfaces.power.v1 → power.proto
    """
    parts = fd.package.split(".")
    if len(parts) >= 3 and parts[0] == "jumpstarter" and parts[1] == "interfaces":
        return f"{parts[2]}.proto"
    return f"{parts[-1]}.proto"


def _proto_mod_name(service_name: str) -> str:
    """Derive the proto module name from the service name.

    NetworkInterface → network_interface_proto
    PowerInterface → power_interface_proto
    """
    return f"{_pascal_to_snake(service_name)}_proto"


def _rust_type(type_name: str, proto_mod: str) -> str:
    """Resolve a proto type to a Rust type, qualified under the proto module."""
    if _is_empty_type(type_name):
        return "()"
    return f"{proto_mod}::{_short_name(_resolve_type_name(type_name))}"


# ---------------------------------------------------------------------------
# Code generation
# ---------------------------------------------------------------------------


def _gen_lib_rs(
    fd: FileDescriptorProto,
    proto_file: str,
) -> str:
    """Generate src/lib.rs with the typed client struct."""
    service = fd.service[0]
    service_name = service.name
    client_name = service_name.replace("Interface", "Client")
    snake_service = _pascal_to_snake(service_name)
    proto_mod = _proto_mod_name(service_name)

    # Determine if we need portforward imports
    has_portforward = any(m.client_streaming for m in service.method)

    lines: list[str] = [
        "// Code generated by jmp proto generate. DO NOT EDIT.",
        "",
        "use std::net::SocketAddr;",
        "",
        "use tonic::transport::Channel;",
        "",
    ]

    if has_portforward:
        lines.append(
            "use jumpstarter_client::portforward::{TcpPortforwardAdapter, UdpPortforwardAdapter};"
        )
    lines.append("use jumpstarter_client::{ExporterSession, UuidInterceptor};")
    lines.append("")

    # Proto module
    lines.extend([
        f"/// Generated protobuf/gRPC types for the {service_name} service.",
        f"pub mod {proto_mod} {{",
        f'    tonic::include_proto!("{fd.package}");',
        "}",
        "",
        f"use {proto_mod}::{snake_service}_client::{service_name}Client as GrpcClient;",
        "",
        "type InterceptedChannel = tonic::service::interceptor::InterceptedService<Channel, UuidInterceptor>;",
        "",
    ])

    # Client struct
    lines.extend([
        f"/// A typed driver client for the `{service_name}` gRPC service.",
        f"pub struct {client_name} {{",
        "    stub: GrpcClient<InterceptedChannel>,",
    ])
    if has_portforward:
        lines.extend([
            "    channel: Channel,",
            "    driver_uuid: String,",
        ])
    lines.extend([
        "}",
        "",
    ])

    # impl block
    lines.extend([
        f"impl {client_name} {{",
        "    /// Create a new client for the named driver instance.",
        "    pub fn new(",
        "        session: &ExporterSession,",
        "        driver_name: &str,",
        "    ) -> Result<Self, Box<dyn std::error::Error + Send + Sync>> {",
        "        let instance = session",
        "            .report()",
        "            .find_by_name(driver_name)",
        '            .ok_or_else(|| format!("driver \'{}\' not found in exporter report", driver_name))?;',
        "        let uuid = instance.uuid().to_owned();",
        "        let channel = session.channel().clone();",
        "        let stub = GrpcClient::with_interceptor(channel.clone(), UuidInterceptor::new(&uuid));",
    ])
    if has_portforward:
        lines.extend([
            "        Ok(Self {",
            "            stub,",
            "            channel,",
            "            driver_uuid: uuid,",
            "        })",
        ])
    else:
        lines.extend([
            "        Ok(Self { stub })",
        ])
    lines.extend([
        "    }",
        "",
    ])

    # Generate methods
    for method in service.method:
        rust_name = _pascal_to_snake(method.name)

        if method.client_streaming:
            # @exportstream — generate TCP and UDP portforward methods
            lines.extend([
                f"    /// Open a TCP port-forwarding adapter for the `{method.name}` stream.",
                f"    pub async fn {rust_name}_tcp(",
                "        &self,",
                "        method: Option<&str>,",
                "    ) -> Result<TcpPortforwardHandle, Box<dyn std::error::Error + Send + Sync>> {",
                "        let adapter = TcpPortforwardAdapter::open_on_channel(",
                "            self.channel.clone(),",
                "            &self.driver_uuid,",
                f'            method.unwrap_or("{rust_name}"),',
                "        )",
                "        .await?;",
                "        Ok(TcpPortforwardHandle { adapter })",
                "    }",
                "",
                f"    /// Open a UDP port-forwarding adapter for the `{method.name}` stream.",
                f"    pub async fn {rust_name}_udp(",
                "        &self,",
                "        method: Option<&str>,",
                "    ) -> Result<UdpPortforwardHandle, Box<dyn std::error::Error + Send + Sync>> {",
                "        let adapter = UdpPortforwardAdapter::open_on_channel(",
                "            self.channel.clone(),",
                "            &self.driver_uuid,",
                f'            method.unwrap_or("{rust_name}"),',
                "        )",
                "        .await?;",
                "        Ok(UdpPortforwardHandle { adapter })",
                "    }",
                "",
            ])
        elif method.server_streaming:
            # Server-streaming → Result<tonic::Streaming<T>, tonic::Status>
            out_type = _rust_type(method.output_type, proto_mod)
            if _is_empty_type(method.input_type):
                lines.extend([
                    f"    /// Server-streaming call for `{method.name}`.",
                    f"    pub async fn {rust_name}(",
                    "        &mut self,",
                    f"    ) -> Result<tonic::Streaming<{out_type}>, tonic::Status> {{",
                    f"        let response = self.stub.{rust_name}(()).await?;",
                    "        Ok(response.into_inner())",
                    "    }",
                    "",
                ])
            else:
                in_type = _rust_type(method.input_type, proto_mod)
                lines.extend([
                    f"    /// Server-streaming call for `{method.name}`.",
                    f"    pub async fn {rust_name}(",
                    "        &mut self,",
                    f"        request: {in_type},",
                    f"    ) -> Result<tonic::Streaming<{out_type}>, tonic::Status> {{",
                    f"        let response = self.stub.{rust_name}(request).await?;",
                    "        Ok(response.into_inner())",
                    "    }",
                    "",
                ])
        else:
            # Unary
            out_type = _rust_type(method.output_type, proto_mod)
            if _is_empty_type(method.input_type):
                lines.extend([
                    f"    /// Unary call for `{method.name}`.",
                    f"    pub async fn {rust_name}(",
                    "        &mut self,",
                    f"    ) -> Result<{out_type}, tonic::Status> {{",
                    f"        let response = self.stub.{rust_name}(()).await?;",
                    "        Ok(response.into_inner())",
                    "    }",
                    "",
                ])
            else:
                in_type = _rust_type(method.input_type, proto_mod)
                lines.extend([
                    f"    /// Unary call for `{method.name}`.",
                    f"    pub async fn {rust_name}(",
                    "        &mut self,",
                    f"        request: {in_type},",
                    f"    ) -> Result<{out_type}, tonic::Status> {{",
                    f"        let response = self.stub.{rust_name}(request).await?;",
                    "        Ok(response.into_inner())",
                    "    }",
                    "",
                ])

    lines.append("}")
    lines.append("")

    # Portforward handle structs (if needed)
    if has_portforward:
        lines.extend([
            "/// Handle to an active TCP port-forwarding session.",
            "///",
            "/// The listener shuts down when this handle is dropped.",
            "pub struct TcpPortforwardHandle {",
            "    adapter: TcpPortforwardAdapter,",
            "}",
            "",
            "impl TcpPortforwardHandle {",
            "    /// The local address the TCP listener is bound to.",
            "    pub fn local_addr(&self) -> SocketAddr {",
            "        self.adapter.local_addr()",
            "    }",
            "}",
            "",
            "/// Handle to an active UDP port-forwarding session.",
            "///",
            "/// The socket shuts down when this handle is dropped.",
            "pub struct UdpPortforwardHandle {",
            "    adapter: UdpPortforwardAdapter,",
            "}",
            "",
            "impl UdpPortforwardHandle {",
            "    /// The local address the UDP socket is bound to.",
            "    pub fn local_addr(&self) -> SocketAddr {",
            "        self.adapter.local_addr()",
            "    }",
            "}",
            "",
        ])

    return "\n".join(lines)


def _gen_build_rs(proto_file: str) -> str:
    """Generate build.rs for tonic-build proto compilation."""
    lines: list[str] = [
        "// Code generated by jmp proto generate. DO NOT EDIT.",
        "",
        "fn main() -> Result<(), Box<dyn std::error::Error>> {",
        "    tonic_build::configure()",
        "        .build_server(false)",
        "        .build_transport(false)",
        "        .compile_protos(",
        f'            &["proto/{proto_file}"],',
        '            &["proto"],',
        "        )?;",
        "    Ok(())",
        "}",
    ]
    return "\n".join(lines)


def _gen_cargo_toml(output_package: str) -> str:
    """Generate Cargo.toml for the driver crate."""
    lines: list[str] = [
        "# Code generated by jmp proto generate. DO NOT EDIT.",
        "",
        "[package]",
        f'name = "{output_package}"',
        'version = "0.1.0"',
        'edition = "2021"',
        "",
        "[dependencies]",
        'jumpstarter-client = "0.1.0"',
        'tonic = "0.13"',
        'prost = "0.13"',
        'tokio = { version = "1", features = ["full"] }',
        'bytes = "1"',
        "",
        "[build-dependencies]",
        'tonic-build = "0.13"',
    ]
    return "\n".join(lines)


# ---------------------------------------------------------------------------
# Main generator function
# ---------------------------------------------------------------------------


def generate_rust_driver_package(
    fd: FileDescriptorProto,
    output_dir: str,
    output_package: str,
    proto_source: str,
) -> dict[str, str]:
    """Generate a complete Rust driver client crate from a FileDescriptorProto.

    Args:
        fd: Parsed proto file descriptor.
        output_dir: Target directory for generated files.
        output_package: Crate name (e.g. "jumpstarter-driver-power").
        proto_source: Raw .proto file content to bundle.

    Returns:
        Mapping of relative_path → file_content.
    """
    if not fd.service:
        return {}

    proto_file = _proto_filename(fd)

    files: dict[str, str] = {}

    # 1. Bundle the .proto source
    files[f"proto/{proto_file}"] = proto_source

    # 2. src/lib.rs — typed client
    files["src/lib.rs"] = _gen_lib_rs(fd, proto_file)

    # 3. build.rs — tonic-build compilation
    files["build.rs"] = _gen_build_rs(proto_file)

    # 4. Cargo.toml
    files["Cargo.toml"] = _gen_cargo_toml(output_package)

    return files


register_language("rust", generate_rust_driver_package)
