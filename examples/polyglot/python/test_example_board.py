"""Example tests for the generated ExampleBoardDevice wrapper.

Run with:
    jmp shell --exporter-config <config> -- pytest examples/polyglot/python/test_example_board.py

These tests demonstrate the typed device wrapper API generated by `jmp codegen`.
Each test method receives a fully-typed `ExampleBoardDevice` with IDE auto-complete
and compile-time type checking for all interface accessors.
"""

import pytest

from jumpstarter_gen.testing.example_board import ExampleBoardTest
from jumpstarter_gen.devices.example_board import ExampleBoardDevice


class TestPowerControl(ExampleBoardTest):
    """Exercise power control via the generated typed wrapper."""

    def test_power_on(self, device: ExampleBoardDevice):
        device.power.on()

    def test_power_off(self, device: ExampleBoardDevice):
        device.power.off()

    def test_read_power_measurements(self, device: ExampleBoardDevice):
        for reading in device.power.read():
            assert reading.voltage >= 0
            assert reading.current >= 0
            break  # Just verify the first reading


class TestStorageMux(ExampleBoardTest):
    """Exercise storage mux switching and write operations."""

    def test_switch_to_host(self, device: ExampleBoardDevice):
        device.storage.host()

    def test_switch_to_dut(self, device: ExampleBoardDevice):
        device.storage.dut()

    def test_storage_off(self, device: ExampleBoardDevice):
        device.storage.off()


class TestNetwork(ExampleBoardTest):
    """Exercise network byte streaming via the EchoNetwork driver."""

    def test_network_is_optional(self, device: ExampleBoardDevice):
        # network is declared optional in the ExporterClass
        # — it may be None if the exporter doesn't provide it
        assert device.network is not None, "Expected network driver in this exporter"

    def test_network_echo(self, device: ExampleBoardDevice):
        if device.network is None:
            pytest.skip("network driver not available")
        # Open a bidi byte stream to the EchoNetwork driver
        with device.network.stream("connect") as conn:
            conn.send(b"hello")
            assert conn.receive() == b"hello"
